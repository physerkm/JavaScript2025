'use strict';

const Car = function (make, speed) {
  this.make = make;
  this.speed = speed;
};

Car.prototype.accelerate = function () {
  this.speed += 10;
  console.log(`${this.make} is going at ${this.speed} km/h`);
};

Car.prototype.brake = function () {
  this.speed -= 5;
  console.log(`${this.make} is going at ${this.speed} km/h`);
};

const toyota = new Car('Toyota', 130);
const honda = new Car('Honda', 90);

toyota.accelerate();
toyota.accelerate();
toyota.brake();
toyota.accelerate();
